const buildInvoicePdfBuffer = (invoice) => {
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({ size: "A4", margin: 50 });
      const chunks = [];

      doc.on("data", (chunk) => chunks.push(chunk));
      doc.on("end", () => resolve(Buffer.concat(chunks)));
      doc.on("error", reject);

      const heading = `Invoice ${invoice.invoiceNumber}`;
      doc.fontSize(22).text(heading, { align: "left" });
      doc.moveDown(0.5);
      doc.fontSize(11).fillColor("#4b5563").text("Generated by BizTrack");
      doc.moveDown(1);

      doc.fillColor("#111827");
      doc.fontSize(12).text(`${invoice.type === "sale" ? "Customer" : "Supplier"}: ${invoice.customerName || "N/A"}`);
      doc.text(`Issue Date: ${new Date(invoice.issueDate).toLocaleDateString()}`);
      doc.text(`Due Date: ${new Date(invoice.dueDate).toLocaleDateString()}`);
      doc.text(`Payment Status: ${invoice.paymentStatus}`);
      doc.moveDown(1);

      const tableStartY = doc.y;
      const itemX = 50;
      const qtyX = 300;
      const priceX = 360;
      const totalX = 460;

      doc.fontSize(12).text("Item", itemX, tableStartY);
      doc.text("Qty", qtyX, tableStartY);
      doc.text("Price", priceX, tableStartY);
      doc.text("Total", totalX, tableStartY);

      doc.moveTo(50, tableStartY + 18).lineTo(545, tableStartY + 18).strokeColor("#d1d5db").stroke();

      let rowY = tableStartY + 26;
      (invoice.items || []).forEach((item) => {
        if (rowY > 740) {
          doc.addPage();
          rowY = 50;
        }
        doc.fontSize(11).fillColor("#111827").text(item.name || "-", itemX, rowY, { width: 240 });
        doc.text(String(item.quantity ?? 0), qtyX, rowY, { width: 40, align: "right" });
        doc.text(formatCurrency(item.price || 0), priceX, rowY, { width: 90, align: "right" });
        doc.text(formatCurrency(item.total || 0), totalX, rowY, { width: 85, align: "right" });
        rowY += 24;
      });

      const totalsY = rowY + 12;
      doc.moveTo(320, totalsY - 8).lineTo(545, totalsY - 8).strokeColor("#d1d5db").stroke();

      doc.fontSize(12).fillColor("#111827");
      doc.text("Subtotal", 360, totalsY, { width: 90, align: "right" });
      doc.text(formatCurrency(invoice.subtotal || 0), 460, totalsY, { width: 85, align: "right" });
      doc.text("Tax", 360, totalsY + 20, { width: 90, align: "right" });
      doc.text(formatCurrency(invoice.tax || 0), 460, totalsY + 20, { width: 85, align: "right" });
      doc.text("Discount", 360, totalsY + 40, { width: 90, align: "right" });
      doc.text(formatCurrency(invoice.discount || 0), 460, totalsY + 40, { width: 85, align: "right" });

      doc.fontSize(14).font("Helvetica-Bold");
      doc.text("Total", 360, totalsY + 68, { width: 90, align: "right" });
      doc.text(formatCurrency(invoice.total || 0), 460, totalsY + 68, { width: 85, align: "right" });

      doc.end();
    } catch (error) {
      reject(error);
    }
  });
};

