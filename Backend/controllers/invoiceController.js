exports.sendInvoiceEmail = async (req, res) => {
  try {
    const invoice = await Invoice.findById(req.params.id);
    if (!invoice) {
      return res.status(404).json({ error: "Invoice not found" });
    }

    const recipientEmail = (req.body?.email || invoice.customerEmail || "").trim();
    if (!recipientEmail) {
      return res.status(400).json({ error: "Recipient email is required" });
    }

    const transporter = createEmailTransporter();
    const subject = `Invoice ${invoice.invoiceNumber} - BizTrack`;
    const pdfBuffer = await buildInvoicePdfBuffer(invoice);
    const text = [
      `Invoice Number: ${invoice.invoiceNumber}`,
      `${invoice.type === "sale" ? "Customer" : "Supplier"}: ${invoice.customerName || "N/A"}`,
      `Total: ${formatCurrency(invoice.total || 0)}`,
      `Issue Date: ${new Date(invoice.issueDate).toLocaleDateString()}`,
      `Due Date: ${new Date(invoice.dueDate).toLocaleDateString()}`,
      `Payment Status: ${invoice.paymentStatus}`,
      "",
      "The invoice PDF is attached with this email.",
    ].join("\n");

    const info = await transporter.sendMail({
      from: process.env.EMAIL_USER,
      to: recipientEmail,
      subject,
      text,
      html: buildInvoiceEmailHtml(invoice),
      attachments: [
        {
          filename: `${invoice.invoiceNumber}.pdf`,
          content: pdfBuffer,
          contentType: "application/pdf",
        },
      ],
    });

    res.json({
      message: `Invoice emailed successfully to ${recipientEmail}`,
      invoiceId: invoice._id,
      recipientEmail,
      messageId: info.messageId,
    });
  } catch (err) {
    res.status(500).json({ error: err.message || "Failed to send invoice email" });
  }
};

