// Get all invoices
exports.getAllInvoices = async (req, res) => {
  try {
    const { 
      type, 
      status, 
      paymentStatus, 
      paymentMethod,
      generatedFrom,
      autoGenerated,
      search, 
      customerName,
      dateFrom,
      dateTo,
      dueDateFrom,
      dueDateTo,
      amountFrom,
      amountTo,
      createdBy,
      sortBy,
      sortOrder,
      relatedId, // Add this for finding invoices by sale/purchase ID
      page = 1, 
      limit = 10 
    } = req.query;
    
    // Build filter object
    const filter = {};
    
    // Basic filters
    if (type) filter.type = type;
    if (status) filter.status = status;
    if (paymentStatus) filter.paymentStatus = paymentStatus;
    if (paymentMethod) filter.paymentMethod = paymentMethod;
    if (generatedFrom) filter.generatedFrom = generatedFrom;
    if (autoGenerated) filter.autoGenerated = autoGenerated === 'true';
    if (relatedId) filter.relatedId = relatedId; // Filter by related sale/purchase ID
    
    // Text search filters
    if (search) {
      filter.$or = [
        { invoiceNumber: { $regex: search, $options: 'i' } },
        { customerName: { $regex: search, $options: 'i' } },
        { customerEmail: { $regex: search, $options: 'i' } },
        { notes: { $regex: search, $options: 'i' } },
      ];
    }
    
    if (customerName) {
      filter.customerName = { $regex: customerName, $options: 'i' };
    }
    
    if (createdBy) {
      filter['createdBy.name'] = { $regex: createdBy, $options: 'i' };
    }
    
    // Date range filters
    if (dateFrom || dateTo) {
      filter.issueDate = {};
      if (dateFrom) filter.issueDate.$gte = new Date(dateFrom);
      if (dateTo) {
        const endDate = new Date(dateTo);
        endDate.setHours(23, 59, 59, 999); // End of day
        filter.issueDate.$lte = endDate;
      }
    }
    
    if (dueDateFrom || dueDateTo) {
      filter.dueDate = {};
      if (dueDateFrom) filter.dueDate.$gte = new Date(dueDateFrom);
      if (dueDateTo) {
        const endDate = new Date(dueDateTo);
        endDate.setHours(23, 59, 59, 999); // End of day
        filter.dueDate.$lte = endDate;
      }
    }
    
    // Amount range filters
    if (amountFrom || amountTo) {
      filter.total = {};
      if (amountFrom) filter.total.$gte = parseFloat(amountFrom);
      if (amountTo) filter.total.$lte = parseFloat(amountTo);
    }

    // Build sort object
    let sort = { createdAt: 1 }; // Default sort - ascending (oldest first)
    if (sortBy) {
      const order = sortOrder === 'asc' ? 1 : -1;
      sort = { [sortBy]: order };
    }

    const skip = (page - 1) * limit;
    const invoices = await Invoice.find(filter)
      .populate('items.inventoryId')
      .populate('relatedId')
      .sort(sort)
      .skip(skip)
      .limit(parseInt(limit));

    const total = await Invoice.countDocuments(filter);

    // Fetch payments from related Sales/Purchases for each invoice
    const invoicesWithPayments = await Promise.all(
      invoices.map(async (invoice) => {
        const invoiceObj = invoice.toObject();
        let payments = [];
        let scheduledAmount = 0;
        
        if (invoice.relatedId) {
          try {
            if (invoice.type === 'sale') {
              const sale = await Sale.findById(invoice.relatedId);
              if (sale) {
                payments = sale.payments || [];
                scheduledAmount = sale.scheduledAmount || 0;
              }
            } else if (invoice.type === 'purchase') {
              const purchase = await Purchase.findById(invoice.relatedId);
              if (purchase) {
                payments = purchase.payments || [];
                scheduledAmount = purchase.scheduledAmount || 0;
              }
            }
          } catch (fetchError) {
            console.error('⚠️ Failed to fetch payments for invoice:', invoice._id, fetchError);
          }
        }
        
        invoiceObj.payments = payments;
        invoiceObj.scheduledAmount = scheduledAmount;
        return invoiceObj;
      })
    );

    res.json({
      invoices: invoicesWithPayments,
      pagination: {
        current: parseInt(page),
        pages: Math.ceil(total / limit),
        total,
      },
    });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};

